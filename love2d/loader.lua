local loaderTicks = 0
local loaderFlash = 0
local loaderTimer = {}

local BUILD = "v1.0"  -- version string

local function DoLoaderDrawer()
    -- Copy colour data to rows 256..511 (the logo area)
    local colourData = loaderColour[loaderFlash + 1]
    Video_CopyColour(colourData, 256, 256)
end

local function DoLoaderTicker()
    loaderFlash = bxor(loaderFlash, Timer_Update(loaderTimer))

    loaderTicks = loaderTicks + 1
    if loaderTicks == 256 then
        Action = Title_Action
    end
end

local function DoLoaderInit()
    Video_PixelFill(0, WIDTH * HEIGHT, 0)
    Video_CopyColour(loaderColour[1], 256, 256)
    Video_Write(184 * WIDTH, "\x02\x07" .. "fawtytoo")
    -- Right-justify build string
    local buildWidth = #BUILD * 8  -- approximate
    Video_Write(184 * WIDTH + WIDTH - buildWidth, "\x02\x01" .. BUILD)

    Timer_Set(loaderTimer, 3, TICKRATE)

    Ticker = DoLoaderTicker
end

local function DoLoaderResponder()
    Action = Title_Action
end

function Loader_Action()
    Responder = DoLoaderResponder
    Ticker    = DoLoaderInit
    Drawer    = DoLoaderDrawer
    Action    = DoNothing
end
