-- ============================================================
-- Level runtime state (initialized by Level_Init)
-- ============================================================
local levelTileType  = {}   -- 512 entries: tile type for each tile
local levelTileGfx   = {}   -- 512 entries: which gfx index (0-9)
local levelGfx       = {}   -- current level graphics [0..9][1..8]
local levelItemCount = 0
local levelCollapseData = {}    -- per-tile collapse counter (8 = full, 0 = gone)
levelBG              = 0        -- background paper colour for this level (global)
local levelCurrent   = nil

levelTicks = 0

-- SPG tile tracking (for level 18)
local spgTileState = {}

local conveyorPhase = 0

function Level_Init()
    local lev = levelData[gameLevel]
    levelCurrent = lev
    levelGfx = lev.gfx
    levelTicks = 0

    -- Build tile type map: data value -> tile type
    local dataToType = {}
    for i, info in ipairs(lev.info) do
        dataToType[i - 1] = info.type  -- 0-based
    end

    -- Init tile types
    for i = 0, 511 do
        local dataVal = lev.data[i + 1] or 0
        levelTileType[i] = dataToType[dataVal] or T_SPACE
        levelTileGfx[i]  = dataVal
    end

    -- Count items
    levelItemCount = 0
    for i = 0, 511 do
        if levelTileType[i] == T_ITEM then
            levelItemCount = levelItemCount + 1
        end
    end

    -- Init collapse data (8 = full tile, counts down as miner stands on it)
    levelBG = rshift(lev.info[1] and lev.info[1].colour or 0, 4)
    for i = 0, 511 do
        levelCollapseData[i] = (levelTileType[i] == T_COLLAPSE) and 8 or 0
    end

    -- Clear game area to level background colour (prevents artifacts from previous level)
    for i = 0, 128 * WIDTH - 1 do
        videoPixel[i] = 0
        System_SetPixel(i, levelBG)
    end

    -- SPG state
    for i = 0, 511 do spgTileState[i] = 0 end

    conveyorPhase = 0
end

function Level_GetTileType(tile)
    if tile < 0 or tile > 511 then return T_VOID end
    return levelTileType[tile]
end

function Level_ReduceItemCount()
    levelItemCount = levelItemCount - 1
    return levelItemCount
end

function Level_TileDelete(tile)
    if tile < 0 or tile > 511 then return end
    levelTileType[tile] = T_SPACE
    levelTileGfx[tile]  = 0
    -- Erase pixel
    local pos = TILE2PIXEL(tile)
    for row = 0, 7 do
        for col = 0, 7 do
            videoPixel[pos + row * WIDTH + col] = 0
        end
    end
    Video_Tile(pos, levelGfx[1] or {0,0,0,0,0,0,0,0}, levelBG, 0, 8)
end

function Level_CollapseTile(tile)
    if tile < 0 or tile > 511 then return end
    if levelTileType[tile] ~= T_COLLAPSE then return end
    levelCollapseData[tile] = levelCollapseData[tile] - 1
    if levelCollapseData[tile] <= 0 then
        Level_TileDelete(tile)
    end
end

function Level_Switch(tile)
    if tile < 0 or tile > 511 then return end
    if levelTileType[tile] == T_SWITCHOFF then
        levelTileType[tile] = T_SWITCHON
        -- redraw as switch-on
        local pos = TILE2PIXEL(tile)
        local lev = levelCurrent
        local gfxIdx = levelTileGfx[tile]
        local ink  = lev.info[gfxIdx + 1] and lev.info[gfxIdx + 1].colour or 0
        Video_Tile(pos, levelGfx[gfxIdx + 1] or {0,0,0,0,0,0,0,0},
                   rshift(ink, 4), band(ink, 0x0f), 8)
    end
end

function Level_SetSpgTile(tile, flag)
    if tile < 0 or tile > 511 then return end
    spgTileState[tile] = flag
end

function Level_GetSpgTile(tile)
    if tile < 0 or tile > 511 then return 0 end
    return spgTileState[tile]
end

function Level_Ticker()
    levelTicks = levelTicks + 1

    -- Advance conveyor phase 2 bits per tick (matches C: << 2 / >> 2 each tick)
    conveyorPhase = band(conveyorPhase + 2, 7)

end

function Level_Drawer()
    local lev = levelCurrent
    if not lev then return end

    for tile = 0, 511 do
        local gfxIdx = levelTileGfx[tile]
        if gfxIdx > 0 then
            local infoEntry = lev.info[gfxIdx + 1]
            local paper, ink = 0, 0
            if infoEntry then
                paper = band(rshift(infoEntry.colour, 4), 0x0f)
                ink   = band(infoEntry.colour, 0x0f)
            end
            local gfx = levelGfx[gfxIdx + 1] or {0,0,0,0,0,0,0,0}
            local ttype = levelTileType[tile]
            local pos = TILE2PIXEL(tile)
            if ttype == T_ITEM then
                ink = bor(band(gameTicks, 6), 1)  -- cycle 1,3,5,7,1,...
            end

            if ttype == T_CONVEYL or ttype == T_CONVEYR then
                -- Rows 0 and 2 (1-indexed: 1 and 3) animate in opposite directions.
                -- All other rows are static (matches C: only two gfx bytes are rotated).
                -- CONVEYL: row 1 goes left, row 3 goes right
                -- CONVEYR: row 1 goes right, row 3 goes left
                local phL = conveyorPhase                   -- left: increasing shift
                local phR = band(8 - conveyorPhase, 7)     -- right: decreasing shift
                local ph1 = (ttype == T_CONVEYL) and phL or phR
                local ph3 = (ttype == T_CONVEYL) and phR or phL
                local shifted = {}
                for r = 1, 8 do
                    local b = gfx[r] or 0
                    if r == 1 and ph1 ~= 0 then
                        shifted[r] = band(bor(lshift(b, ph1), rshift(b, 8 - ph1)), 0xff)
                    elseif r == 3 and ph3 ~= 0 then
                        shifted[r] = band(bor(lshift(b, ph3), rshift(b, 8 - ph3)), 0xff)
                    else
                        shifted[r] = b
                    end
                end
                Video_Tile(pos, shifted, paper, ink, 8)
            elseif ttype == T_COLLAPSE and levelCollapseData[tile] < 8 then
                -- Draw top portion (solid rows), blank bottom rows
                local rows = math.max(0, levelCollapseData[tile])
                local spaceGfx = levelGfx[1] or {0,0,0,0,0,0,0,0}
                local nextPos = Video_Tile(pos, gfx, paper, ink, rows)
                Video_Tile(nextPos, spaceGfx, levelBG, 0, 8 - rows)
            else
                Video_Tile(pos, gfx, paper, ink, 8)
            end
        end
    end
end

